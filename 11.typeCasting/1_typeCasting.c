// type casting
// covert one data tye into another
// syntax : --->     (type) value

// cast int value into float

#include <stdio.h>
int main()
{
    int n = 9 / 4;
    printf("int : %d\n", n);

    float f = (float)9 / 4;

    printf("float: %f\n", f);

    return 0;
}
