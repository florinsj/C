// library funtions

// library functions are the inbuilt functions in C that are grouped and placed in library
// all C library functions are defined in different header files  with extension   .h

// stdio.h  -- standard input/output header file  -- printf,scanf ,etc
// conio.h  -- console input/output header file
// string.h -- contains all string related library functions  -- gets() , puts()...
// stdlib.h -- contains all general library functions like malloc(), calloc(),exit() , etc
// math.h   -- contains all the math operations related functions  fdf(), pow(), ..etc
// time.h   -- contains all the time related functions
// ctype.h  -- contains all the character handling functions
// stdarg.h -- contains variable argument functions
// signal.h -- contains all the signal functions
// setjmp.h -- contains all the jump functions
// locale.h -- contains locale functions
// errno.h  -- contains error handling functions
// assert.h -- contains diagnostic functions
